<h1>canyon 峡 <span style="font-size: 50%">——環境に生きる者が環境をコントロールする</span></h1>
<p>
　下流の渓谷を守る二つのダムの紹介です。<br>
　群馬県藤岡市譲原と埼玉県寄居町の間にある下久保ダムは下流に三波石峡（さんばせききょう）を持つダムです。三波石とは、この地域一帯で採れる白い縞模様を持つ美しい石のことです。発電のために地下へ水を送るこの下久保ダムが建設されたあと、長いあいだ川の水が枯れた状態が続き、魚も、魚を餌にする鳥もいなくなってしまいました。現在はダムから川へ水を送るよう工事を行い、三波石峡に美しい景観と生き物も戻っています。<br>
　高津戸ダムは群馬県みどり市にあるダムです。下流にはダム名と同じ高津戸峡が広がっています。荒々しいゴツゴツした岩と、鮮やかな紅葉のコントラスト。自然の美しさにしみじみと感動しました。<br>
　ダムめぐりでは、ダムそのものやダム湖に目が行きがちですが、周辺の環境にも見ることはとても大切です。ダムによってどのような環境の変化があったのか知ることは、私たちの生きる場所について学ぶことにもつながっていきます。
</p>
