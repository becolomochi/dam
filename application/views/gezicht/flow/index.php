<h1>flow 流 <span style="font-size: 50%">——無数の形を作り上げる　水のアーティスト</span></h1>
<p>
　最後の章は、放流するダムです。奈良俣ダムと矢木沢ダムの管理所のウェブサイトに二〇十五年六月の点検放流の告知が掲載されました。普段なかなか見ることのできない奈良俣ダムと矢木沢ダムの放流。これは行くしかないと思い、カメラを持って出かけました。<br>
　奈良俣ダムは晴れた日には陽の光を反射し、クリーム色の体が白く輝く美しいダムです。放流もおしとやかで美しく、レース編みのような白い紋様が斜面を滑り落ちていきます。<br>
　三種類の形式からなる矢木沢ダムは大きなダム。遠くからも見えるアーチダムの部分から実際放流をするスキージャンプ洪水吐（こうずいばき）はアーチの部分から徒歩で三十分ほど手前にあります。時間だけでどれだけ大きいか理解できるはずです。点検放流当日は、矢木沢ダムならではの圧倒的な放流を多くの人々が眺めていました。<br>
　最後は須田貝ダム。管理の都合上、近くまで寄ってダムの上を歩いて…ということはできませんが、この日は緑に囲まれた中での清々しい放流を見ることができました。<br>
　ダムは放流することで水を無数の形に変化させます。静から動へ、大きな水流を生み出す姿はまさにアーティストです。
</p>
