<h1>wit 白 <span style="font-size: 50%">——ゆきどけ　春の大地へ</span></h1>
<p>　三月。市街地はどんどん暖かくなっていきますが、山の中腹〜頂上近くにあるダムの周りは暖かさがまだ遠く、山々は白い縞模様をまとっています。<br>
　まず紹介するダムは、市の名前の付いた大町ダムと、大町ダムよりさらに標高の高い場所にある七倉ダム。どちらも長野県にあります。大町ダムの龍神湖は、エメラルドグリーンの美しい湖。今も残る伝承ように、下流の市街地を守る龍が本当に住んでいるかのよう。七倉ダムは、大町ダムとは見た目が異なっています。ゴツゴツとした荒々しい岩肌は柔らかな雪に埋もれていました。<br>
　場所は変わり、群馬県沼田市の薗原ダムと平出ダム。薗原ダムの湖は春の暖かい日差しに見守られながら、ゆっくりと氷が解けていました。平出ダムは小さなダム。車一台の幅ギリギリのトンネルをくぐった先にあるので、探検隊になったような気分でダムめぐりができました。出かけた日にはひとつゲートを開けほんの少し放流をしていました。<br>
　ダムたちは、暑くなる季節まで、解けた雪を湖に貯めてゆきます。
</p>
