<h1>kalm 和 <span style="font-size: 50%">——生き物の暮らしと共に</span></h1>
<p>
　人間や動物の生活に寄り添うダムののんびりとした姿です。<br>
　徐々に観光産業として見世物になったり、見学する場所としての役割を担っているダムですが、次に紹介するダムは市街地にほど近い場所で黙々と働いているダムたちです。<br>
　埼玉県の北部、寄居町にある円良田ダム。湖のへりにはお店がならんでいます。湖上では釣りを楽しむ人々の姿を見ることができました。湖の周りは桜が満開になるそうです。<br>
　神水ダムは群馬県側から下久保ダム（峡の章に掲載）に行く途中にある小さな目立たないダムです。しかし、下流に広がる川と緑のすがすがしい風景はこのダムの上でしか見ることができません。<br>
　円良田ダムのすぐそばにある発電専用の玉淀ダム。近くには橋が多くかかっています。人の生活を便利にする建造物のコラボレーションです。
　茂沢ダムは場所が変わって渋川市にあります。賑わう市街地を抜けた先の住宅地の中にあるダムで、近所に住む人々が散歩をしたり湖には大量の鴨がいたり、のどかな時間が流れていました。
</p>
